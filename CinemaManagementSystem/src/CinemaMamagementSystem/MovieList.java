    private void comboBoxTitleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxTitleActionPerformed
        // TODO add your handling code here:
        
            try {
            String query = "SELECT * FROM regMovie WHERE title=? ";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1,(String) comboBoxTitle.getSelectedItem());

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                mt = rs.getString("title");
                textFieldDirector.setText(rs.getString("director"));
                textFieldCast.setText(rs.getString("billcast"));
                textFieldDate.setText(rs.getString("releasedate"));
                textFieldDuration.setText(rs.getString("duration"));
                textFieldGenre.setText(rs.getString("genre"));
                textFieldRating.setText(rs.getString("rating"));
                textFieldDistributor.setText(rs.getString("distributor"));
                textFieldWebsite.setText(rs.getString("website"));
                textAreaSynopsis.setText(rs.getString("synopsis"));

            }

            pst.close();

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_comboBoxTitleActionPerformed

