public void setComboBoxTitle() {
    conn = MySQLConnection.dbConnector();
    if (conn == null) {
        JOptionPane.showMessageDialog(null, "Kết nối database chưa mở!");
        return;
    }

    try (PreparedStatement pst = conn.prepareStatement("SELECT title FROM regMovie");
         ResultSet rs = pst.executeQuery()) {

        comboBoxTitle.removeAllItems();  // xóa item cũ
        while (rs.next()) {
            comboBoxTitle.addItem(rs.getString("title"));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Lỗi khi load titles: " + e.getMessage());
    }
}

